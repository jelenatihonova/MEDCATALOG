function getLang() {
    let path = window.location.pathname;

    if (path.includes("/RU/")) return "RU";
    if (path.includes("/LV/")) return "LV";
    if (path.includes("/LT/")) return "LT";
    if (path.includes("/EE/")) return "EE";

    return ""; // EN = корень
}

function searchDrugs(input) {
    const query = input.value.toLowerCase();
    const resultsBox = input.parentElement.querySelector(".searchResults");

    if (!query) {
        resultsBox.style.display = "none";
        return;
    }

    const lang = getLang();

    const filtered = drugs.filter(d =>
        d.name.toLowerCase().includes(query)
    ).slice(0, 20);

    resultsBox.innerHTML = "";

    filtered.forEach(item => {
        const div = document.createElement("div");

        let base = lang ? `/${lang}/` : `/`;

        div.innerHTML = `<a href="${base + item.url}" style="display:block;padding:6px;text-decoration:none;color:#000;">
            ${item.name}
        </a>`;

        resultsBox.appendChild(div);
    });

    resultsBox.style.display = "block";
}